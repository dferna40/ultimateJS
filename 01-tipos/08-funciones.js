// Declarar la funcion
function saludar() {
    console.log("Hola mundo");
}

// Llamar a la funcion
saludar();

function suma() {
    return 2+2;
}

// Primera forma de obtener el resultado de una funcion
let resultado = suma();

console.log(resultado);

// Segunda forma de obtener el resultado de una funcion
console.log(suma());